--=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=--
--	Class Player: Represents a player and stores information about players 
--=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=--
local Player = {}
Player.__index = Player;


-------------------------------------------------------------------------------
--  Player:new : Creates a new player profile
-------------------------------------------------------------------------------
function Player:new()
	local obj = {}
	
	obj._name = nil;
	obj._password = nil;
	obj._dyzkCollection = {}

	return setmetatable(obj, self);
end


-------------------------------------------------------------------------------
--  Player:SetName : Sets the name of the player
-------------------------------------------------------------------------------
function Player:SetName( name )
	self._name = name;
end


-------------------------------------------------------------------------------
--  Player:SetPassword : Sets the password for the player
-------------------------------------------------------------------------------
function Player:SetPassword( pass )
	self._password = pass;
end


--===========================================================================--
--  Initialization
--===========================================================================--
return Player