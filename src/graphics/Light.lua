local Light = {}
Light.__index = Light;


-------------------------------------------------------------------------------
--  Light:new : Creates a new Light
-------------------------------------------------------------------------------
function Light:new()
	local obj = {}
	
	obj.position = {0,0,0}
	obj.diffuse  = {0,0,0,1}
	obj.ambient  = {0,0,0,0}
	obj.strength = 0;

	return setmetatable(obj, self);
end



--===========================================================================--
--  Initialization
--===========================================================================--
return Light