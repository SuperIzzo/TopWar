--===========================================================================--
--  Dependencies
--===========================================================================--
local BaseObject 			= require 'src.gui.BaseObject'


--=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=--
--	Class Panel: a brief... 
--=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=--
local Panel = {}
Panel.__index = setmetatable( Panel, BaseObject );


-------------------------------------------------------------------------------
--  Panel:new : Creates a new Panel
-------------------------------------------------------------------------------
function Panel:new()
	local obj = BaseObject:new();

	return setmetatable(obj, self);
end



--===========================================================================--
--  Initialization
--===========================================================================--
return Panel